var avg = 20;
var percentage = 90;
console.log("the avg is:" + avg + "the percentage is:" + percentage);
//logical and &&
var res1 = ((avg > 40) && (percentage > 80));
console.log("the result is:" + res1);
//logical or ||
var res2 = ((avg > 40) || (percentage > 80));
console.log("the result is:" + res2);
//logical not !
var res3 = !((avg > 40) && (percentage > 80));
console.log("the result is:" + res3);
