var avg:number=20;
var percentage:number=90;
console.log("the avg is:" + avg +"the percentage is:" + percentage);

//logical and &&
var res1:boolean=((avg>40)&&(percentage>80));
console.log("the result is:" + res1);

//logical or ||
var res2:boolean=((avg>40)||(percentage>80));
console.log("the result is:" + res2);

//logical not !
var res3:boolean=!((avg>40)&&(percentage>80));
console.log("the result is:" + res3);